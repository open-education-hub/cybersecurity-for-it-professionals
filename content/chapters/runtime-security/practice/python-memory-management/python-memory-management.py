#!/usr/bin/python3

# input() reads user input
# print() prints user input

# Python can read user input with the input() function
# Read string into `a`
a = input()

# print contents of `a`
print('Variable a is:', a)

# print length of `a`
print('Length of a is:', len(a))

# Value of `a` can be overwritten
a = input()

print('New value of a is:', a)
print('New length of a is: ', len(a))
